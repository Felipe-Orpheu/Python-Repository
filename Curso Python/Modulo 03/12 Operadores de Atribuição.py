print(x)
x **= 2 # Mesmo que x = x²
print(x)
x /= 9 # Mesmo que x = x / 9
print(x)
x %= 9 # Mesmo que x = x % 9
print(x)
x //= 5 # Mesmo que x = x // 5 
print(x)