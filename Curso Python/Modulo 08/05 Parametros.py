# Parametros de uma função
# Podemos ter funçoes com ou sem parametros

def imprime_nome(nome, sobrenome): # parametro é o nome dado aos valores ultilizados na definição de uma função
    #corpo da função
    print(f"Ola, {nome} {sobrenome}")


nome = "maria"
imprime_nome("maria", "clara", 25) # argumento é o nome dado aos valores ultilizados na chamada de uma função
